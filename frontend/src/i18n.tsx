const dictionaries: Record<Lang, Dict> = { ru, en };

interface I18nValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = typeof localStorage !== 'undefined' ? localStorage.getItem('lang') : null;
    if (saved === 'en' || saved === 'ru') return saved;
    // First visit: pick the language from the browser, defaulting to English
    // for any non-Russian locale.
    const nav = typeof navigator !== 'undefined' && navigator.language ? navigator.language.toLowerCase() : '';
    return nav.startsWith('ru') ? 'ru' : 'en';
  });

  useEffect(() => {
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang;
  }, [lang]);

  const t = (key: string, vars?: Record<string, string | number>): string => {
    const dict = dictionaries[lang] ?? ru;
    let s: string = dict[key] ?? ru[key] ?? key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
      }
    }
    return s;
  };

  const setLang = (l: Lang) => setLangState(l);

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used within I18nProvider');
  return ctx;
}

export function LanguageToggle() {
  const { lang, setLang } = useI18n();
  return (
    <button
      onClick={() => setLang(lang === 'ru' ? 'en' : 'ru')}
      className="flex items-center gap-1.5 px-3 py-2 rounded-lg text-sm text-gray-400 hover:text-white hover:bg-gray-800 whitespace-nowrap"
      aria-label={lang === 'ru' ? 'Switch to English' : 'Переключить на русский'}
      title={lang === 'ru' ? 'Switch to English' : 'Переключить на русский'}
    >
      <span className={lang === 'ru' ? 'font-semibold text-white' : ''}>RU</span>
      <span className="text-gray-600">/</span>
      <span className={lang === 'en' ? 'font-semibold text-white' : ''}>EN</span>
    </button>
  );
}
